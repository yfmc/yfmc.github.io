/* kakaomap API */

function kakaoMap() {
	// 지도를 담을 영역의 DOM 레퍼런스
	var container=document.getElementById('kakaomap');

	// 지점명
	var branch=map_json[getTheaterId()].brand+" "+map_json[getTheaterId()].branch;

	// 좌표
	var lat=map_json[getTheaterId()].lat;
	var lng=map_json[getTheaterId()].lng;

	// 위치 변수
	var position=new kakao.maps.LatLng(lat, lng);

	// 지도를 생성할 때 필요한 옵션
	var options={
	    center: position,	// 지도의 중심좌표
	    level: 3            // 지도의 레벨
	}

	// 지도 생성 및 객체 리턴
	var map=new kakao.maps.Map(container, options);

	// 마커 생성
	var marker=new kakao.maps.Marker({
	    position: position
	});

	// 마커를 지도에 표시
	marker.setMap(map);

	// 마커에 클릭이벤트를 등록
	kakao.maps.event.addListener(marker, 'click', function() {
	    window.open("https://map.kakao.com/link/to/"+branch+","+lat+","+lng);
	});

	// 지도타입 전환 컨트롤을 생성 및 지도 우측 상단에 표시
	var mapTypeControl=new kakao.maps.MapTypeControl();
	map.addControl(mapTypeControl, kakao.maps.ControlPosition.TOPRIGHT);

	// 줌 컨트롤 생성 및 우측에 표시
	var zoomControl=new kakao.maps.ZoomControl();
	map.addControl(zoomControl, kakao.maps.ControlPosition.RIGHT);
}

// modal이 띄워지면 kakaomap 함수 실행하기
$(function() {
	$('#modal_map').on('shown.bs.modal', kakaoMap);
});